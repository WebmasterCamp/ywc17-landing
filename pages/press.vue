  border-radius: 1000px;
  height: 48px;
  background: ${props => color[props.color].darker};
  background: ${props => color[props.color].gradient};
  color: ${props => props.color === 'yellow' ? 'black' : 'white'};
  margin-top: 5px;
  max-width: 200px;

  &:hover {
    background: linear-gradient(rgba(0,0,0,0.25),rgba(0,0,0,0.25)), ${props => color[props.color].gradient};
  }
  &:active {
    background: ${props => color[props.color].darker};
    filter: brightness(75%);
  }
`
export default {
  layout: 'secondary',
  head () {
    return {
      title: 'ร่วมประชาสัมพันธ์โครงการ - 17th Young Webmaster Camp',
      meta: [
        { hid: 'og:title', name: 'og:title', content: 'ร่วมประชาสัมพันธ์โครงการ - 17th Young Webmaster Camp' },
        { hid: 'og:description', name: 'og:description', content: '' }, // TODO: Add description
        { hid: 'description', name: 'description', content: '' } // TODO: Add description
      ]
    }
  },
  components: {
    SectionHead,
    BannerItem,
    BannerCol,
    BannerButton,
    Footer: () => import('~/components/sections/Footer')
  },
  data () {
    return {
      bannerList: {
        '160x600': false,
        '300x50': false,
        '300x250': false,
        '300x600': false,
        '320x50': false,
        '320x100': false,
        '728x90': false,
        '970x250': false,
        '1200x628': false
      }
    }
  },
  methods: {
    selectText (event, ref) {
      const elm = ref ? this.$refs[ref] : event.target
      if (document.selection) { // IE
        const range = document.body.createTextRange()
        range.moveToElementText(elm)
        range.select()
      } else if (window.getSelection) {
        const range = document.createRange()
        range.selectNode(elm)
        window.getSelection().removeAllRanges()
        window.getSelection().addRange(range)
      }
    },
    viewCode (size) {
      if (this.bannerList[size] === true) {
        this.bannerList[size] = false
      } else {
        this.bannerList[size] = true
      }
    },
    getSrc (size, notLocal) {
      let ext = 'gif'
      if (size === '1200x628') {
        ext = 'png'
      }
      return `${notLocal ? 'https://ywc17.ywc.in.th' : ''}/images/banner/${size}.${ext}`
    },
    getBannerCode (src, withoutATag) {
      const imgTag = `<img src="${src}" alt="17th Young Webmaster Camp">`
      if (withoutATag) {
        return imgTag
      }
      return `<a href="https://bit.ly/YWC17-banner">${imgTag}</a>`
    }
  }
}
</script>
