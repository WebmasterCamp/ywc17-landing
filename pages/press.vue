const BannerCol = styled('div')`
  display: inline-grid;
  align-content: start;
  text-align: left;
  a {
    box-sizing: border-box;
  }
  img {
    max-width: 95%;
    max-height: 50vh;
    object-fit: contain;
  }
  
  @media screen and (max-width:768px) {
    justify-content: center;
    margin-top: 10px;
  }
`
const BannerButton = styled('a', { color: String })`
  width: 100%;
  height: 100%;
  font-family: 'Maledpan', 'Sarabun';
  font-weight: bold;
  font-size: 18px;
  border: none;
